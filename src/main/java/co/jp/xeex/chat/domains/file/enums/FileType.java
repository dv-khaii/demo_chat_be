/**
 * Represents the type of a file.
 */
public enum FileType {
    /**
     * Represents an image file type.
     */
    IMAGE,

    /**
     * Represents an audio file type.
     */
    AUDIO,

    /**
     * Represents a video file type.
     */
    VIDEO,

    /**
     * Represents a generic file type that is not an image, audio, or video.
     */
    FILE
}
